package java_fundamental_01;

public class Type_conversion {
    public static void main(String [] args){
        byte b = 127;
        int a = b;

        System.out.println(a);

        byte a = 15;
        byte b = 13;
         int result = a * b;

         System.out.println(result);

         float f = 5.6f;
         int t =(int) f;

         System.out.print(t);
    }
}
